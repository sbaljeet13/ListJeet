        newManagedObject.setValue(date, forKey: "timeStamp")
        newManagedObject.setValue(location, forKey: "location")
        newManagedObject.setValue(detail, forKey: "detail")
        newManagedObject.setValue(image, forKey: "cellImage")
        
        
        // Save the context.
        do {
